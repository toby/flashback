function extractFeeling(entry) {
  const lines = getSectionLines(entry, 2, 'Year thesis');
  const line = lines.find((item) => /The feeling of the year:/i.test(item));
  const match = line?.match(/The feeling of the year:\s+\*\*(.+?)\*\*/i);
  return match ? stripInlineMarkdown(match[1]) : '';
}

function extractThesis(entry) {
  const feeling = extractFeeling(entry);
  if (feeling) {
    return `${entry.year}: ${feeling}.`;
  }

  const firstParagraph = firstParagraph(getSectionLines(entry, 2, 'Year thesis'));
  return firstParagraph || entry.frontmatter.subtitle;
}

function extractDesignClimate(entry) {
  const dominantLines = getSectionLines(entry, 3, 'The dominant tension');
  const dominantParagraph = firstParagraph(dominantLines);
  const emerging = extractBullets(getSectionLines(entry, 3, 'What is emerging'));

  return {
    summary: dominantParagraph || entry.frontmatter.subtitle,
    emerging,
  };
}

function extractTimeline(entry) {
  const lines = getSectionLines(entry, 2, 'Timeline signals');
  const rows = [];

  for (const line of lines) {
    const match = line.match(/^\|\s*(.+?)\s*\|\s*(.+?)\s*\|$/);
    if (!match) {
      continue;
    }

    const [signal, meaning] = match.slice(1).map((value) => stripInlineMarkdown(value));
    if (
      signal.toLowerCase() === 'signal' ||
      signal.includes('---') ||
      meaning.includes('---')
    ) {
      continue;
    }

    rows.push({ signal, meaning });
  }

  return rows;
}

function extractRecipes(entry) {
  const recipeParent = findHeading(entry, 2, 'Flashback design recipes');
  if (!recipeParent) {
    return [];
  }

  return entry.headings
    .filter((heading) => heading.level === 3 && heading.lineIndex > recipeParent.lineIndex)
    .filter((heading) => {
      const nextH2 = entry.headings.find((item) => item.level === 2 && item.lineIndex > recipeParent.lineIndex);
      return !nextH2 || heading.lineIndex < nextH2.lineIndex;
    })
    .map((heading) => {
      const match = heading.text.match(/^Recipe\s+(\d+):\s+(.+)$/);
      if (!match) {
        return null;
      }

      const lines = getSectionLinesByHeading(entry, heading);
      const fields = {};
      let useFor = '';

      for (const line of lines) {
        const useForMatch = line.match(/^Use for:\s*(.+)$/);
        if (useForMatch) {
          useFor = stripInlineMarkdown(useForMatch[1]);
          continue;
        }

        const fieldMatch = line.match(/^-\s+([^:]+):\s*(.+)$/);
        if (!fieldMatch) {
          continue;
        }

        fields[normalizeRecipeField(fieldMatch[1])] = stripInlineMarkdown(fieldMatch[2]);
      }

      return {
        number: Number(match[1]),
        name: stripInlineMarkdown(match[2]),
        heading: heading.text,
        slug: heading.slug,
        useFor,
        palette: fields.palette ?? '',
        type: fields.type ?? '',
        layout: fields.layout ?? '',
        imagery: fields.imagery ?? '',
        motion: fields.motion ?? '',
        risk: fields.risk ?? '',
        addAccuracyWith: fields.addAccuracyWith ?? '',
      };
    })
    .filter(Boolean);
}

function extractPrompts(entry) {
  const lines = getSectionLines(entry, 2, 'Design prompt seeds');
  const prompts = [];
  let current = null;

  for (const line of lines) {
    if (line.startsWith('```')) {
      if (current) {
        prompts.push(current.join('\n').trim());
        current = null;
      } else {
        current = [];
      }
      continue;
    }

    if (current) {
      current.push(line);
    }
  }

  return prompts.filter(Boolean);
}

