function buildSite(entries) {
  rmSync(SITE_DIR, { recursive: true, force: true });
  mkdirSync(SITE_DIR, { recursive: true });

  // .nojekyll keeps GitHub Pages from processing the tree, so corpus .md files
  // are served raw for the agent to fetch and example->corpus links resolve.
  writeFileSync(join(SITE_DIR, '.nojekyll'), '');
  writeFileSync(join(SITE_DIR, 'index.html'), generateIndexHtml(entries));

  const siteExamples = join(SITE_DIR, 'examples');
  const siteCorpus = join(SITE_DIR, 'corpus');
  mkdirSync(siteExamples, { recursive: true });
  mkdirSync(siteCorpus, { recursive: true });
  writeFileSync(join(siteExamples, 'manifest.json'), `${JSON.stringify(buildManifest(entries), null, 2)}\n`);

  for (const entry of entries) {
    const yearDir = join(siteExamples, String(entry.year));
    mkdirSync(yearDir, { recursive: true });
    writeFileSync(join(yearDir, 'index.html'), generateExampleHtml(entry));
    writeFileSync(join(siteCorpus, `${entry.year}.md`), entry.source);
  }

  const siteSkill = join(SITE_DIR, 'skill');
  mkdirSync(siteSkill, { recursive: true });
  const skill = readSkill();
  writeFileSync(join(siteSkill, 'SKILL.md'), skill.source);
  writeFileSync(join(siteSkill, 'index.html'), generateSkillPageHtml(skill, entries));

  console.log(`Built _site/ for ${entries.length} year${entries.length === 1 ? '' : 's'} (${relative(ROOT, SITE_DIR)}).`);
}

// Resolve a year's full art direction from its `art_direction` frontmatter,
// falling back to DEFAULT_ART for anything a draft omits. Unknown font/texture/
// ornament/layout names fall back rather than throwing so rendering never
// crashes; validateEntries reports them for published years.
function resolveArtDirection(entry) {
  const raw = entry.frontmatter.art_direction;
  const ad = raw && typeof raw === 'object' && !Array.isArray(raw) ? raw : {};
  const fb = DEFAULT_ART;
  const bg = Array.isArray(ad.bg) && ad.bg.length >= 3 ? ad.bg.slice(0, 3) : fb.bg;
  const accents = Array.isArray(ad.accents) && ad.accents.length >= 4 ? ad.accents.slice(0, 4) : fb.accents;

  return {
    stamp: ad.stamp ?? String(entry.frontmatter.decade_position ?? fb.stamp),
    note: ad.note ?? entry.frontmatter.subtitle ?? fb.note,
    palette: {
      ink: ad.ink ?? fb.ink,
      paper: ad.paper ?? fb.paper,
      muted: ad.muted ?? fb.muted,
      bg,
      accents,
    },
    fonts: {
      display: resolveFont(ad.display, fb.display),
      body: resolveFont(ad.body, fb.body),
      mono: resolveFont(ad.mono, fb.mono),
    },
    layout: LAYOUTS[ad.layout] ? ad.layout : fb.layout,
    texture: TEXTURES[ad.texture] ? ad.texture : fb.texture,
    ornament: ORNAMENTS[ad.ornament] ? ad.ornament : fb.ornament,
  };
}

function resolveFont(name, fallbackName) {
  const key = FONT_LIBRARY[name] ? name : (FONT_LIBRARY[fallbackName] ? fallbackName : 'system-sans');
  return { name: key, ...FONT_LIBRARY[key] };
}

// Legacy theme shape consumed by the index cards and the example base tokens.
function getTheme(entry) {
  const art = resolveArtDirection(entry);
  const [bgA, bgB, bgC] = art.palette.bg;
  const [accentA, accentB, accentC, accentD] = art.palette.accents;

  return {
    stamp: art.stamp,
    note: art.note,
    ink: art.palette.ink,
    paper: art.palette.paper,
    muted: art.palette.muted,
    bgA,
    bgB,
    bgC,
    accentA,
    accentB,
    accentC,
    accentD,
    art,
  };
}

function fontStylesheetLinks(fonts) {
  const families = [...new Set(Object.values(fonts).map((font) => font.family).filter(Boolean))];
  if (families.length === 0) {
    return '';
  }

  const href = `https://fonts.googleapis.com/css2?${families.map((family) => `family=${family}`).join('&')}&display=swap`;
  return `
  <link rel="preconnect" href="https://fonts.googleapis.com">
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
  <link rel="stylesheet" href="${href}">`;
}

