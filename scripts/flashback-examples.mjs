function buildSectionMap(entry) {
  const productHeading = entry.headings.find((heading) => heading.level === 2 && heading.text.startsWith('Product and '));
  const previousYearHeading = entry.headings.find((heading) => heading.level === 2 && /^How \d{4} differs from \d{4}$/.test(heading.text));
  const sections = [
    { heading: findHeading(entry, 2, 'Year thesis'), label: 'Year thesis', fallback: entry.feeling },
    { heading: previousYearHeading, label: previousYearHeading ? previousYearHeading.text.replace(/^How /, '').replace(' differs from ', ' to ') : '', fallback: 'Year-to-year change.' },
    { heading: findHeading(entry, 2, 'Design climate'), label: 'Design climate', fallback: entry.designClimate.summary },
    { heading: findHeading(entry, 2, 'Timeline signals'), label: 'Timeline signals', fallback: entry.timeline.slice(0, 4).map((item) => item.signal).join(', ') },
    { heading: findHeading(entry, 2, 'Typography'), label: 'Typography' },
    { heading: findHeading(entry, 2, 'Graphic design'), label: 'Graphic design' },
    { heading: productHeading, label: 'Product design' },
    { heading: findHeading(entry, 2, 'Architecture and interiors'), label: 'Architecture' },
    { heading: findHeading(entry, 2, 'Fashion and self-design'), label: 'Fashion' },
    { heading: findHeading(entry, 2, 'Music'), label: 'Music' },
    { heading: findHeading(entry, 2, 'Film and moving image'), label: 'Film' },
    { heading: findHeading(entry, 2, 'Color, material, and surface'), label: 'Surface' },
    { heading: findHeading(entry, 2, 'Anti-cliches'), label: 'Anti-cliches' },
  ];

  return sections
    .filter((section) => section.heading)
    .map((section) => ({
      label: section.label,
      slug: section.heading.slug,
      description: shorten(section.fallback || firstParagraph(getSectionLinesByHeading(entry, section.heading)), 92),
    }));
}

function findHeading(entry, level, text) {
  return entry.headings.find((heading) => heading.level === level && heading.text === text);
}

function getSectionLines(entry, level, text) {
  const heading = findHeading(entry, level, text);
  return heading ? getSectionLinesByHeading(entry, heading) : [];
}

function getSectionLinesByHeading(entry, heading) {
  const nextHeading = entry.headings.find((candidate) => (
    candidate.lineIndex > heading.lineIndex && candidate.level <= heading.level
  ));
  const endIndex = nextHeading ? nextHeading.lineIndex : entry.lines.length;
  return entry.lines.slice(heading.lineIndex + 1, endIndex);
}

function extractBullets(lines) {
  return lines
    .map((line) => line.match(/^\s*-\s+(.+)$/)?.[1])
    .filter(Boolean)
    .map((line) => stripInlineMarkdown(line).replace(/\.$/, ''));
}

function firstParagraph(lines) {
  const paragraphs = [];
  let current = [];

  for (const line of lines) {
    const trimmed = line.trim();

    if (
      trimmed === '' ||
      trimmed.startsWith('|') ||
      trimmed.startsWith('- ') ||
      trimmed.startsWith('```') ||
      /^#{1,6}\s/.test(trimmed)
    ) {
      if (current.length > 0) {
        paragraphs.push(current.join(' '));
        current = [];
      }
      continue;
    }

    current.push(trimmed);
  }

  if (current.length > 0) {
    paragraphs.push(current.join(' '));
  }

  return stripInlineMarkdown(paragraphs[0] ?? '');
}

function normalizeRecipeField(field) {
  const normalized = field.trim().toLowerCase();

  if (normalized === 'add accuracy with') {
    return 'addAccuracyWith';
  }

  return normalized.replace(/-([a-z])/g, (_, letter) => letter.toUpperCase());
}

function stripInlineMarkdown(value) {
  return value
    .replace(/\[\^.+?\]/g, '')
    .replace(/\[(.+?)\]\(.+?\)/g, '$1')
    .replace(/[*_`]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function slugify(value) {
  return stripInlineMarkdown(value)
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/['"]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-');
}

function shorten(value, maxLength) {
  const stripped = stripInlineMarkdown(value);
  if (stripped.length <= maxLength) {
    return stripped;
  }

  return `${stripped.slice(0, maxLength - 1).trim()}...`;
}

function collectCorpusHrefs(html) {
  return Array.from(html.matchAll(/href="([^"]*corpus\/\d{4}\.md(?:#[^"]+)?)"/g), (match) => match[1]);
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function escapeAttribute(value) {
  return escapeHtml(value);
}

