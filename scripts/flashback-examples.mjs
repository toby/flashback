function main() {
  const command = process.argv[2];

  if (!['sync', 'check', 'site'].includes(command)) {
    console.error('Usage: node scripts/flashback-examples.mjs <sync|check|site>');
    process.exit(1);
  }

  const entries = readCorpusEntries().filter((entry) => SYNC_STATUSES.has(entry.frontmatter.status));
  const errors = validateEntries(entries);

  if (errors.length > 0) {
    reportErrors(errors);
    process.exit(1);
  }

  if (command === 'sync') {
    syncExamples(entries);
    return;
  }

  if (command === 'site') {
    buildSite(entries);
    return;
  }

  checkExamples(entries);
}

function readCorpusEntries() {
  if (!existsSync(CORPUS_DIR)) {
    throw new Error(`Missing corpus directory: ${relative(ROOT, CORPUS_DIR)}`);
  }

  return readdirSync(CORPUS_DIR)
    .filter((file) => /^\d{4}\.md$/.test(file))
    .sort()
    .map((file) => parseCorpusFile(join(CORPUS_DIR, file)));
}

function parseCorpusFile(filePath) {
  const source = readFileSync(filePath, 'utf8');
  const lines = source.split(/\r?\n/);
  const fileYear = Number(filePath.match(/(\d{4})\.md$/)?.[1]);

  if (lines[0] !== '---') {
    throw new Error(`${relative(ROOT, filePath)} is missing frontmatter`);
  }

  const endFrontmatter = lines.findIndex((line, index) => index > 0 && line === '---');
  if (endFrontmatter === -1) {
    throw new Error(`${relative(ROOT, filePath)} has unterminated frontmatter`);
  }

  const frontmatter = parseFrontmatter(lines.slice(1, endFrontmatter));
  const headings = parseHeadings(lines);
  const headingBySlug = new Map(headings.map((heading) => [heading.slug, heading]));
  const year = Number(frontmatter.year);

  if (year !== fileYear) {
    throw new Error(`${relative(ROOT, filePath)} frontmatter year ${year} does not match filename ${fileYear}`);
  }

  const entry = {
    filePath,
    source,
    lines,
    frontmatter,
    headings,
    headingBySlug,
    year,
  };

  const feeling = extractFeeling(entry);
  const thesis = extractThesis(entry);
  const designClimate = extractDesignClimate(entry);
  const timeline = extractTimeline(entry);
  const recipes = extractRecipes(entry);
  const prompts = extractPrompts(entry);
  const references = extractReferences(entry);
  const antiCliches = extractAntiCliches(entry);
  const parsedEntry = {
    ...entry,
    feeling,
    thesis,
    designClimate,
    timeline,
    recipes,
    prompts,
    references,
    antiCliches,
  };
  parsedEntry.sectionMap = buildSectionMap(parsedEntry);

  return parsedEntry;
}

// Minimal YAML reader for corpus frontmatter. Supports top-level `key: scalar`,
// top-level `key:` + `- item` arrays, and one level of nested mapping
// (`key:` then indented `subkey: scalar` or `subkey:` + `- item`). This is
// enough for the `art_direction` block; it intentionally avoids a YAML
// dependency to keep the toolchain self-contained.
function parseFrontmatter(lines) {
  const data = {};
  let topKey = null;
  let topKind = null;
  let nestedArrayKey = null;

  for (const rawLine of lines) {
    if (rawLine.trim() === '') {
      continue;
    }

    const [, indentText, content] = rawLine.match(/^(\s*)(.*)$/);
    const indented = indentText.length > 0;

    if (!indented) {
      nestedArrayKey = null;
      const keyValue = content.match(/^([A-Za-z0-9_]+):(?:\s*(.*))?$/);
      if (!keyValue) {
        topKey = null;
        topKind = null;
        continue;
      }

      const [, key, rawValue = ''] = keyValue;
      if (rawValue.trim() === '') {
        data[key] = [];
        topKey = key;
        topKind = 'pending';
      } else {
        data[key] = parseScalar(rawValue);
        topKey = null;
        topKind = null;
      }
      continue;
    }

    if (!topKey) {
      continue;
    }

    const arrayItem = content.match(/^-\s+(.+)$/);
    if (arrayItem && nestedArrayKey === null && (topKind === 'pending' || topKind === 'array')) {
      topKind = 'array';
      data[topKey].push(parseScalar(arrayItem[1]));
      continue;
    }

    const nestedKeyValue = content.match(/^([A-Za-z0-9_]+):(?:\s*(.*))?$/);
    if (nestedKeyValue) {
      if (topKind !== 'map') {
        data[topKey] = {};
        topKind = 'map';
      }

      const [, nestedKey, nestedRaw = ''] = nestedKeyValue;
      if (nestedRaw.trim() === '') {
        data[topKey][nestedKey] = [];
        nestedArrayKey = nestedKey;
      } else {
        data[topKey][nestedKey] = parseScalar(nestedRaw);
        nestedArrayKey = null;
      }
      continue;
    }

    if (arrayItem && topKind === 'map' && nestedArrayKey) {
      data[topKey][nestedArrayKey].push(parseScalar(arrayItem[1]));
    }
  }

  return data;
}

function parseScalar(value) {
  const trimmed = value.trim();

  if (/^\d+$/.test(trimmed)) {
    return Number(trimmed);
  }

  if (
    (trimmed.startsWith('"') && trimmed.endsWith('"')) ||
    (trimmed.startsWith("'") && trimmed.endsWith("'"))
  ) {
    return trimmed.slice(1, -1);
  }

  return trimmed;
}

function parseHeadings(lines) {
  const slugCounts = new Map();
  const headings = [];

  lines.forEach((line, lineIndex) => {
    const match = line.match(/^(#{1,6})\s+(.+?)\s*#*\s*$/);
    if (!match) {
      return;
    }

    const text = stripInlineMarkdown(match[2]);
    const baseSlug = slugify(text);
    const count = slugCounts.get(baseSlug) ?? 0;
    slugCounts.set(baseSlug, count + 1);

    headings.push({
      level: match[1].length,
      text,
      slug: count === 0 ? baseSlug : `${baseSlug}-${count}`,
      lineIndex,
    });
  });

  return headings;
}

function validateEntries(entries) {
  const errors = [];

  for (const entry of entries) {
    const path = relative(ROOT, entry.filePath);

    for (const field of REQUIRED_FRONTMATTER) {
      if (
        entry.frontmatter[field] === undefined ||
        entry.frontmatter[field] === '' ||
        (Array.isArray(entry.frontmatter[field]) && entry.frontmatter[field].length === 0)
      ) {
        errors.push(`${path} is missing required frontmatter field: ${field}`);
      }
    }

    for (const section of REQUIRED_SECTIONS) {
      if (!findHeading(entry, 2, section)) {
        errors.push(`${path} is missing required section: ${section}`);
      }
    }

    if (entry.recipes.length === 0) {
      errors.push(`${path} must include at least one Flashback design recipe`);
    }

    for (const recipe of entry.recipes) {
      if (!recipe.useFor) {
        errors.push(`${path} ${recipe.heading} is missing a "Use for:" line`);
      }

      for (const field of ['palette', 'type', 'layout', 'imagery', 'motion', 'risk']) {
        if (!recipe[field]) {
          errors.push(`${path} ${recipe.heading} is missing recipe field: ${field}`);
        }
      }
    }

    if (entry.prompts.length === 0) {
      errors.push(`${path} must include at least one fenced prompt under Design prompt seeds`);
    }

    for (const group of ['objects', 'printAndGraphics', 'spaces']) {
      if (entry.references[group].length === 0) {
        errors.push(`${path} Reference artifacts is missing items for ${group}`);
      }
    }

    validateArtDirection(entry, path, errors);
  }

  return errors;
}

// Published years must declare a complete `art_direction` block so each page
// reads as its own era. Names are checked against the font/texture/ornament/
// layout registries; colors must be hex so generated CSS stays valid.
function validateArtDirection(entry, path, errors) {
  if (entry.frontmatter.status !== 'example') {
    return;
  }

  const ad = entry.frontmatter.art_direction;
  if (!ad || typeof ad !== 'object' || Array.isArray(ad)) {
    errors.push(`${path} is missing the art_direction frontmatter block (required for status: example)`);
    return;
  }

  for (const [role, value] of [['display', ad.display], ['body', ad.body], ['mono', ad.mono]]) {
    if (!value) {
      errors.push(`${path} art_direction.${role} is required`);
    } else if (!FONT_LIBRARY[value]) {
      errors.push(`${path} art_direction.${role} references unknown font "${value}". Known: ${Object.keys(FONT_LIBRARY).join(', ')}`);
    }
  }

  for (const [field, registry] of [['layout', LAYOUTS], ['texture', TEXTURES], ['ornament', ORNAMENTS]]) {
    if (!ad[field]) {
      errors.push(`${path} art_direction.${field} is required`);
    } else if (!registry[ad[field]]) {
      errors.push(`${path} art_direction.${field} references unknown "${ad[field]}". Known: ${Object.keys(registry).join(', ')}`);
    }
  }

  for (const key of ['ink', 'paper', 'muted']) {
    if (!isHexColor(ad[key])) {
      errors.push(`${path} art_direction.${key} must be a hex color (got ${JSON.stringify(ad[key])})`);
    }
  }

  if (!Array.isArray(ad.bg) || ad.bg.length < 3 || !ad.bg.every(isHexColor)) {
    errors.push(`${path} art_direction.bg must be a list of at least 3 hex colors`);
  }

  if (!Array.isArray(ad.accents) || ad.accents.length < 4 || !ad.accents.every(isHexColor)) {
    errors.push(`${path} art_direction.accents must be a list of at least 4 hex colors`);
  }
}

function isHexColor(value) {
  return typeof value === 'string' && /^#[0-9a-fA-F]{3,8}$/.test(value.trim());
}

function syncExamples(entries) {
  mkdirSync(EXAMPLES_DIR, { recursive: true });
  promoteSyncedStatuses(entries);
  cleanupObsoleteExamples(entries);
  writeFileSync(MANIFEST_PATH, `${JSON.stringify(buildManifest(entries), null, 2)}\n`);

  for (const entry of entries) {
    const yearDir = join(EXAMPLES_DIR, String(entry.year));
    mkdirSync(yearDir, { recursive: true });
    writeFileSync(join(yearDir, 'index.html'), generateExampleHtml(entry));
  }

  writeFileSync(INDEX_PATH, generateIndexHtml(entries));

  console.log(`Synced ${entries.length} example year${entries.length === 1 ? '' : 's'}.`);
}

function checkExamples(entries) {
  const errors = [];
  const expectedManifest = `${JSON.stringify(buildManifest(entries), null, 2)}\n`;

  if (!existsSync(MANIFEST_PATH)) {
    errors.push('examples/manifest.json is missing. Run npm run sync:examples.');
  } else if (readFileSync(MANIFEST_PATH, 'utf8') !== expectedManifest) {
    errors.push('examples/manifest.json is stale. Run npm run sync:examples.');
  }

  if (!existsSync(INDEX_PATH)) {
    errors.push('index.html is missing. Run npm run sync:examples.');
  } else if (readFileSync(INDEX_PATH, 'utf8') !== generateIndexHtml(entries)) {
    errors.push('index.html is stale. Run npm run sync:examples.');
  }

  const corpusYears = new Set(entries.map((entry) => String(entry.year)));

  for (const entry of entries) {
    const examplePath = join(EXAMPLES_DIR, String(entry.year), 'index.html');
    const relativeExamplePath = relative(ROOT, examplePath);
    const expectedHtml = generateExampleHtml(entry);

    if (!existsSync(examplePath)) {
      errors.push(`${relativeExamplePath} is missing. Run npm run sync:examples.`);
      continue;
    }

    if (readFileSync(examplePath, 'utf8') !== expectedHtml) {
      errors.push(`${relativeExamplePath} is stale. Run npm run sync:examples.`);
    }

    if (entry.frontmatter.status !== 'example') {
      errors.push(`${relative(ROOT, entry.filePath)} has an example page but status is "${entry.frontmatter.status}", not "example".`);
    }

    for (const href of collectCorpusHrefs(expectedHtml)) {
      const [, linkedYear, linkedSlug] = href.match(/^\.\.\/\.\.\/corpus\/(\d{4})\.md(?:#(.+))?$/) ?? [];
      if (!linkedYear) {
        continue;
      }

      if (Number(linkedYear) !== entry.year) {
        errors.push(`${relativeExamplePath} links to corpus/${linkedYear}.md, expected corpus/${entry.year}.md`);
      }

      if (linkedSlug && !entry.headingBySlug.has(linkedSlug)) {
        errors.push(`${relativeExamplePath} links to missing corpus anchor: #${linkedSlug}`);
      }
    }
  }

  if (existsSync(EXAMPLES_DIR)) {
    for (const name of readdirSync(EXAMPLES_DIR)) {
      const examplePath = join(EXAMPLES_DIR, name, 'index.html');
      if (/^\d{4}$/.test(name) && existsSync(examplePath) && !corpusYears.has(name)) {
        errors.push(`examples/${name}/index.html has no matching corpus/${name}.md`);
      }
    }
  }

  if (errors.length > 0) {
    reportErrors(errors);
    process.exit(1);
  }

  console.log(`Examples are current for ${entries.length} year${entries.length === 1 ? '' : 's'}.`);
}

